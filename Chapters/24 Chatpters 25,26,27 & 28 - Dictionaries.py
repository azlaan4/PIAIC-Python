# A dictionary works something like a list, but instead of a simple series of things,
# a dictionary is a series of pairs of things. Each pair contains a key—"first
# name", "last name" etc.—and a value—"Azlaan", "Zubair", etc.

user_info = {"fname": "Azlaan", "lastname": "Zubair", "address": "Karachi, Pakistan."}

print("Here is the dictionary output:", user_info)
print("Here is the dictionary output:", user_info["fname"])
print("Here is the dictionary output:", user_info["lname"])